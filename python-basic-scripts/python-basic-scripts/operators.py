# Experiment with arithmetic and logical operators in Python

# Arithmetic
print("Addition:", 5 + 3)
print("Subtraction:", 10 - 2)
print("Multiplication:", 4 * 2)
print("Division:", 8 / 2)
print("Modulus:", 10 % 3)
print("Exponent:", 2 ** 3)
print("Floor Division:", 7 // 2)

# Logical
a = True
b = False
print("AND:", a and b)
print("OR:", a or b)
print("NOT A:", not a)
